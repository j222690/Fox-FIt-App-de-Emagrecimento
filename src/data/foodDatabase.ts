export function findFood(query: string): FoodItem | undefined {
  const q = query.toLowerCase().trim();
  // Try exact match first
  const exact = foodDatabase.find(f => f.name === q);
  if (exact) return exact;
  // Then partial match
  return foodDatabase.find(f => q.includes(f.name) || f.name.includes(q));
}

export function parseMealInput(input: string): { food: FoodItem; grams: number }[] {
  const parts = input.split('+').map(s => s.trim());
  const results: { food: FoodItem; grams: number }[] = [];
  
  for (const part of parts) {
    const gramMatch = part.match(/(\d+)\s*g/i);
    const grams = gramMatch ? parseInt(gramMatch[1]) : 0;
    const cleanName = part.replace(/\d+\s*g/gi, '').trim();
    const food = findFood(cleanName);
    if (food) {
      results.push({ food, grams });
    }
  }
  return results;
}

export function calculateMacros(food: FoodItem, grams: number) {
  const factor = grams / 100;
  return {
    calories: Math.round(food.calories * factor),
    protein: Math.round(food.protein * factor * 10) / 10,
    carbs: Math.round(food.carbs * factor * 10) / 10,
    fat: Math.round(food.fat * factor * 10) / 10,
  };
}
